# sort nums in numerical order
nums = [5, 0, 4, 7, 32, 1, 77, 5, 0]
print('nums:', nums)
print('sorted nums:', sorted(nums))

# sort names in alphabetical order nb capital letters order before lower-case letters
names = ['Hussein', 'amad', 'alwiyia', 'saadia', 'Rahma', 'alwiyia', 'Hussein']
print('names:', names)
print('sorted names:', sorted(names))

# sets filter duplicates from a list

# removes duplicates but does not preserve original order
print('set of nums:', set(nums))

# removes duplicates but does not preserve original order
print('set of names:', set(names))
