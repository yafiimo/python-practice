# create variables for numbers
ten = 10
five = 5

# operations
ten * five      # multiplication = 50
ten / five      # division returns float = 2.0
ten // five     # division returns integer (rounded down) = 2
ten + five      # addition = 15
ten - five      # subtraction = 5
ten ** 5        # power of = 100000
ten % 5         # remainder = 0

type(ten)       # returns <class 'int'>
type(10.0)      # returns <class 'float'>
